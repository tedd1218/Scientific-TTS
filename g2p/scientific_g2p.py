# Parse CMUDict — take first pronunciation for words with multiple
def load_cmudict(path):
    dictionary = {}
    with open(path, 'r') as f:
        for line in f:
            line = line.strip()
            if not line or line.startswith(';;;'):
                continue
            parts = line.split()
            word = parts[0].lower()
            # Skip alternate pronunciations (CMUDict marks them as word(2), word(3))
            if '(' in word:
                continue
            phonemes = ' '.join(parts[1:])
            if word not in dictionary:  # keep first pronunciation only
                dictionary[word] = phonemes
    return dictionary


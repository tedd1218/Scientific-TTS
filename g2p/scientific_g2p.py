def build_sentence_pairs(sentences, word_dictionary, min_coverage=0.85):
    """
    Convert sentences to (grapheme_sentence, phoneme_sentence) pairs.
    Only includes words that appear in the dictionary — skips unknown words
    in both the grapheme AND phoneme sequence so they stay aligned.
    """
    pairs = []
    dict_words = set(word_dictionary.keys())

    for sent in tqdm(sentences, desc="Building pairs"):
        sent = sent.strip().lower()
        words = [w for w in re.findall(r'[a-zA-Z]+', sent) if w]
        if not words or len(words) < 3:
            continue

        covered = [w for w in words if w in dict_words]
        if len(covered) / len(words) < min_coverage:
            continue

        # Only include words that are in the dictionary (keeps G/P aligned)
        grapheme_parts = []
        phoneme_parts  = []
        for w in words:
            if w in word_dictionary:
                grapheme_parts.append(w)
                phoneme_parts.append(word_dictionary[w])

        if len(grapheme_parts) >= 3:  # skip if too few words survive
            pairs.append((' '.join(grapheme_parts), ' | '.join(phoneme_parts)))

    return pairs


"""## Section 8: Evaluate — Phoneme Error Rate (PER)

**Phoneme Error Rate (PER)** is the standard metric for G2P systems.
It is computed as the edit distance (insertions + deletions + substitutions)
between predicted and ground-truth phoneme sequences, divided by the length of the ground truth.

Lower is better. The paper achieves ~2.5% PER on British English with transfer learning vs ~27% from scratch.
"""

@torch.no_grad()
def greedy_decode(model, src, max_len=MAX_SEQ_LEN):
    """
    Greedy decoding: at each step, pick the most probable next phoneme.
    Returns a list of phoneme index sequences (one per sample in batch).
    """
    model.eval()
    src = src.to(DEVICE)
    src_pad_mask = (src == PAD_IDX)
    memory = model.encode(src, src_key_padding_mask=src_pad_mask)

    batch_size = src.size(0)
    # Start with <sos>
    tgt = torch.full((batch_size, 1), SOS_IDX, dtype=torch.long, device=DEVICE)
    finished = torch.zeros(batch_size, dtype=torch.bool, device=DEVICE)

    for _ in range(max_len):
        tgt_mask = model.make_causal_mask(tgt.size(1))
        logits = model.decode(tgt, memory, tgt_mask=tgt_mask,
                               memory_key_padding_mask=src_pad_mask)
        next_token = logits[:, -1, :].argmax(dim=-1, keepdim=True)  # (B, 1)
        tgt = torch.cat([tgt, next_token], dim=1)
        finished |= (next_token.squeeze(-1) == EOS_IDX)
        if finished.all():
            break

    # Convert to lists, stripping SOS/EOS/PAD
    results = []
    for seq in tgt.cpu().tolist():
        clean = [t for t in seq if t not in (SOS_IDX, EOS_IDX, PAD_IDX)]
        results.append(clean)
    return results


def compute_per(model, test_pairs, char2idx, phoneme2idx, idx2phoneme, batch_size=64):
    """
    Compute Phoneme Error Rate on a test set.
    Uses dynamic programming (same as word error rate but on phonemes).
    """
    from jiwer import wer as _wer

    dataset = G2PDataset(test_pairs, char2idx, phoneme2idx)
    loader  = DataLoader(dataset, batch_size=batch_size, collate_fn=collate_fn)

    all_pred, all_ref = [], []

    for src, tgt in tqdm(loader, desc="Evaluating"):
        pred_seqs = greedy_decode(model, src)

        for pred_ids, tgt_ids in zip(pred_seqs, tgt.tolist()):
            pred_phonemes = [idx2phoneme.get(i, '<unk>') for i in pred_ids
                             if i not in (SOS_IDX, EOS_IDX, PAD_IDX)]
            ref_phonemes  = [idx2phoneme.get(i, '<unk>') for i in tgt_ids
                             if i not in (SOS_IDX, EOS_IDX, PAD_IDX)]

            if ref_phonemes:
                # jiwer expects strings; we join phonemes with spaces
                all_pred.append(' '.join(pred_phonemes))
                all_ref.append(' '.join(ref_phonemes))

    per = _wer(all_ref, all_pred) * 100  # jiwer WER = PER when tokens are phonemes
    return per, all_pred, all_ref



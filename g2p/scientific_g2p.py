def prepare_for_finetuning(pretrained_path, src_vocab_size, tgt_vocab_size):
    """
    Load a pretrained checkpoint and re-initialise the target-side layers.
    This implements Figure 1 from the paper:
      - Encoder weights:     LOADED from checkpoint (frozen knowledge)
      - Decoder weights:     LOADED from checkpoint
      - tgt_embedding:       RE-INITIALISED (new phoneme set)
      - fc_out:              RE-INITIALISED (new output size)
    """
    # Create fresh model with same vocab sizes
    ft_model = G2PTransformer(
        src_vocab_size=src_vocab_size,
        tgt_vocab_size=tgt_vocab_size,
    ).to(DEVICE)

    # Load pretrained weights
    state_dict = torch.load(pretrained_path, map_location=DEVICE)

    # Load all weights except the target-side output layers
    # (they may have different sizes if phoneme vocab changed)
    model_state = ft_model.state_dict()
    for key, val in state_dict.items():
        if key in model_state and model_state[key].shape == val.shape:
            model_state[key] = val
        else:
            print(f"  Re-initialising: {key} (shape mismatch or new layer)")

    ft_model.load_state_dict(model_state)
    return ft_model



# Generate IPA pronunciations for all terms using eSpeak-NG
# This is the automated (noisy) annotation step

def generate_ipa_dictionary(terms, language='en-us', batch_size=100):
    """
    Generate IPA pronunciations for a list of words using eSpeak-NG.
    Returns a dict: {word: ipa_string}
    Filters out words where eSpeak-NG returns empty or suspicious results.
    """
    backend = EspeakBackend(language, with_stress=True)
    sep = Separator(word=' ', phone='|')

    dictionary = {}
    failed = []

    # Process in batches to avoid memory issues
    for i in tqdm(range(0, len(terms), batch_size), desc="Generating IPA"):
        batch = terms[i:i+batch_size]
        try:
            ipa_batch = backend.phonemize(batch, separator=sep)
            for word, ipa in zip(batch, ipa_batch):
                ipa_clean = ipa.strip()
                # Filter: skip if IPA is empty or suspiciously short for the word
                if ipa_clean and len(ipa_clean) >= 2:
                    # Convert | separators to spaces for phoneme sequences
                    phoneme_seq = ' '.join(ipa_clean.replace('|', ' ').split())
                    dictionary[word] = phoneme_seq
                else:
                    failed.append(word)
        except Exception as e:
            failed.extend(batch)

    print(f"✓ Dictionary built: {len(dictionary)} entries, {len(failed)} failed")
    return dictionary, failed


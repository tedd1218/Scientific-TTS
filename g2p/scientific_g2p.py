"""## Section 5: Define the Transformer Model

We implement the same architecture as the paper:
- 3-layer Transformer encoder + 3-layer Transformer decoder
- 256-dimensional hidden states, 8 attention heads
- ~4.1M parameters
"""

class PositionalEncoding(nn.Module):
    """
    Adds positional information to token embeddings.
    Since Transformers have no built-in sense of sequence order
    (unlike RNNs), we inject position via sinusoidal encodings.
    """
    def __init__(self, d_model, dropout=0.1, max_len=512):
        super().__init__()
        self.dropout = nn.Dropout(p=dropout)

        pe = torch.zeros(max_len, d_model)
        position = torch.arange(0, max_len).unsqueeze(1).float()
        div_term = torch.exp(torch.arange(0, d_model, 2).float() * (-math.log(10000.0) / d_model))
        pe[:, 0::2] = torch.sin(position * div_term)
        pe[:, 1::2] = torch.cos(position * div_term)
        pe = pe.unsqueeze(0)  # (1, max_len, d_model)
        self.register_buffer('pe', pe)

    def forward(self, x):
        x = x + self.pe[:, :x.size(1)]
        return self.dropout(x)


class G2PTransformer(nn.Module):
    """
    Sequence-to-sequence Transformer for G2P conversion.
    Matches the architecture in Engelhart et al. (2021):
      - Encoder: character embeddings → 3-layer Transformer encoder
      - Decoder: phoneme embeddings → 3-layer Transformer decoder → FC output

    The FC output layer and phoneme embedding are re-initialised for fine-tuning
    when switching to a new phoneme set (as in the paper, Figure 1).
    """
    def __init__(self, src_vocab_size, tgt_vocab_size,
                 d_model=256, nhead=8,
                 num_encoder_layers=3, num_decoder_layers=3,
                 dim_feedforward=512, dropout=0.1):
        super().__init__()

        self.d_model = d_model

        # Encoder side: character embeddings
        self.src_embedding = nn.Embedding(src_vocab_size, d_model, padding_idx=PAD_IDX)
        self.src_pos_enc   = PositionalEncoding(d_model, dropout)

        # Decoder side: phoneme embeddings (re-initialised for transfer learning)
        self.tgt_embedding = nn.Embedding(tgt_vocab_size, d_model, padding_idx=PAD_IDX)
        self.tgt_pos_enc   = PositionalEncoding(d_model, dropout)

        # Core Transformer
        self.transformer = nn.Transformer(
            d_model=d_model,
            nhead=nhead,
            num_encoder_layers=num_encoder_layers,
            num_decoder_layers=num_decoder_layers,
            dim_feedforward=dim_feedforward,
            dropout=dropout,
            batch_first=True,
            norm_first=False  # Post-LN (Layer Norm), as in original Transformer
        )

        # Output projection: hidden → phoneme vocabulary (re-initialised for transfer learning)
        self.fc_out = nn.Linear(d_model, tgt_vocab_size)

        self._init_weights()

    def _init_weights(self):
        """Xavier uniform initialisation, same as the paper."""
        for p in self.parameters():
            if p.dim() > 1:
                nn.init.xavier_uniform_(p)

    def make_causal_mask(self, sz):
        """Upper-triangular mask to prevent decoder attending to future tokens."""
        return torch.triu(torch.ones(sz, sz), diagonal=1).bool().to(DEVICE)

    def encode(self, src, src_key_padding_mask=None):
        src_emb = self.src_pos_enc(self.src_embedding(src) * math.sqrt(self.d_model))
        return self.transformer.encoder(src_emb, src_key_padding_mask=src_key_padding_mask)

    def decode(self, tgt, memory, tgt_mask=None, tgt_key_padding_mask=None,
               memory_key_padding_mask=None):
        tgt_emb = self.tgt_pos_enc(self.tgt_embedding(tgt) * math.sqrt(self.d_model))
        out = self.transformer.decoder(
            tgt_emb, memory,
            tgt_mask=tgt_mask,
            tgt_key_padding_mask=tgt_key_padding_mask,
            memory_key_padding_mask=memory_key_padding_mask
        )
        return self.fc_out(out)

    def forward(self, src, tgt):
        src_pad_mask = (src == PAD_IDX)  # (B, S)
        tgt_pad_mask = (tgt == PAD_IDX)  # (B, T)
        tgt_causal   = self.make_causal_mask(tgt.size(1))

        memory = self.encode(src, src_key_padding_mask=src_pad_mask)
        logits = self.decode(tgt, memory,
                             tgt_mask=tgt_causal,
                             tgt_key_padding_mask=tgt_pad_mask,
                             memory_key_padding_mask=src_pad_mask)
        return logits  # (B, T, tgt_vocab_size)



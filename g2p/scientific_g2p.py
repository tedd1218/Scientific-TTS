# Build character and phoneme vocabularies
#
# The model treats G2P as sequence-to-sequence:
#   INPUT:  character sequence  e.g. ['p','h','o','t','o','s','y','n','t','h','e','s','i','s']
#   OUTPUT: phoneme sequence    e.g. ['f','oʊ','t','oʊ','s','ɪ','n','θ','ə','s','ɪ','s']
#
# Special tokens:
#   <pad>  — padding to fixed length
#   <sos>  — start of sequence (decoder input)
#   <eos>  — end of sequence
#   <unk>  — unknown token

SPECIAL_TOKENS = ['<pad>', '<sos>', '<eos>', '<unk>']
PAD_IDX, SOS_IDX, EOS_IDX, UNK_IDX = 0, 1, 2, 3

def build_vocab(pairs, mode='char'):
    """Build vocabulary from (grapheme, phoneme) pairs.
    mode='char' for grapheme vocab, mode='phoneme' for phoneme vocab.
    """
    tokens = set()
    for grapheme, phoneme in pairs:
        if mode == 'char':
            tokens.update(list(grapheme))  # character-level
        else:
            tokens.update(phoneme.split())  # space-separated phonemes
    vocab = SPECIAL_TOKENS + sorted(tokens)
    token2idx = {t: i for i, t in enumerate(vocab)}
    idx2token = {i: t for t, i in token2idx.items()}
    return vocab, token2idx, idx2token


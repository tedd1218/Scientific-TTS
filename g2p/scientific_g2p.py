def train_epoch(model, loader, optimizer, criterion, clip=1.0):
    model.train()
    total_loss = 0
    for src, tgt in loader:
        src, tgt = src.to(DEVICE), tgt.to(DEVICE)
        # Teacher forcing: decoder input is tgt[:-1], target is tgt[1:]
        tgt_in  = tgt[:, :-1]
        tgt_out = tgt[:, 1:]

        optimizer.zero_grad()
        logits = model(src, tgt_in)  # (B, T-1, vocab)

        # Flatten for cross-entropy
        loss = criterion(logits.reshape(-1, logits.size(-1)), tgt_out.reshape(-1))
        loss.backward()
        torch.nn.utils.clip_grad_norm_(model.parameters(), clip)
        optimizer.step()
        total_loss += loss.item()
    return total_loss / len(loader)


@torch.no_grad()
def eval_epoch(model, loader, criterion):
    model.eval()
    total_loss = 0
    for src, tgt in loader:
        src, tgt = src.to(DEVICE), tgt.to(DEVICE)
        tgt_in  = tgt[:, :-1]
        tgt_out = tgt[:, 1:]
        logits = model(src, tgt_in)
        loss = criterion(logits.reshape(-1, logits.size(-1)), tgt_out.reshape(-1))
        total_loss += loss.item()
    return total_loss / len(loader)


def train_model(model, train_loader, val_loader, n_epochs, save_path,
                lr=0.0005, patience=5):
    """Train with Adam optimiser and save best checkpoint by validation loss."""
    optimizer = torch.optim.Adam(model.parameters(), lr=lr)
    criterion = nn.CrossEntropyLoss(ignore_index=PAD_IDX)

    best_val_loss = float('inf')
    history = {'train': [], 'val': []}
    no_improve = 0

    for epoch in range(1, n_epochs + 1):
        t0 = time.time()
        train_loss = train_epoch(model, train_loader, optimizer, criterion)
        val_loss   = eval_epoch(model, val_loader, criterion)
        elapsed    = time.time() - t0

        history['train'].append(train_loss)
        history['val'].append(val_loss)

        if val_loss < best_val_loss:
            best_val_loss = val_loss
            torch.save(model.state_dict(), save_path)
            flag = '  ← best'
            no_improve = 0
        else:
            flag = ''
            no_improve += 1

        print(f"Epoch {epoch:3d}/{n_epochs} | "
              f"train {train_loss:.4f} | val {val_loss:.4f} | "
              f"{elapsed:.1f}s{flag}")

        if no_improve >= patience:
            print(f"Early stopping (no improvement for {patience} epochs)")
            break

    return history


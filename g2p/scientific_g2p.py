def encode_pair(grapheme, phoneme, char2idx, phoneme2idx, max_len=MAX_SEQ_LEN):
    """Convert a (grapheme, phoneme) string pair to integer index tensors."""
    # Grapheme: character by character
    g_ids = [char2idx.get(c, UNK_IDX) for c in grapheme][:max_len-2]
    g_ids = [SOS_IDX] + g_ids + [EOS_IDX]

    # Phoneme: token by token (space-separated)
    p_ids = [phoneme2idx.get(p, UNK_IDX) for p in phoneme.split()][:max_len-2]
    p_ids = [SOS_IDX] + p_ids + [EOS_IDX]

    return g_ids, p_ids


class G2PDataset(Dataset):
    def __init__(self, pairs, char2idx, phoneme2idx, max_len=MAX_SEQ_LEN):
        self.samples = []
        for g, p in pairs:
            g_ids, p_ids = encode_pair(g, p, char2idx, phoneme2idx, max_len)
            self.samples.append((g_ids, p_ids))

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx):
        return self.samples[idx]


def collate_fn(batch):
    """Pad sequences in a batch to the same length."""
    g_seqs, p_seqs = zip(*batch)
    g_lens = [len(s) for s in g_seqs]
    p_lens = [len(s) for s in p_seqs]

    g_padded = torch.zeros(len(batch), max(g_lens), dtype=torch.long)
    p_padded = torch.zeros(len(batch), max(p_lens), dtype=torch.long)

    for i, (g, p) in enumerate(zip(g_seqs, p_seqs)):
        g_padded[i, :len(g)] = torch.tensor(g)
        p_padded[i, :len(p)] = torch.tensor(p)

    return g_padded, p_padded


